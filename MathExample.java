public class MathExample {
    public static void main(String[] args) {
        System.out.println(Math.max(5, 10)); // Prints the maximum of 5 and 10, which is 10
        System.out.println(Math.min(5, 10)); // Prints the minimum of 5 and 10, which is 5
        System.out.println(Math.sqrt(64)); // Prints the square root of 64, which is 8
    }
}
